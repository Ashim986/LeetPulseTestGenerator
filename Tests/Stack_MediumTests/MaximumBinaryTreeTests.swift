private class Solution {
    func constructMaximumBinaryTree(_ nums: [Int]) -> TreeNode? {
        let n = nums.count
        if n == 0 {
            return nil
        }
        var stack: [TreeNode] = []
        for num in nums {
            let node = TreeNode(num)
            while !stack.isEmpty, stack.last?.val ?? 0 < num {
                node.left = stack.removeLast()
            }
            if !stack.isEmpty {
                stack.last?.right = node
            }
            stack.append(node)
        }
        return stack.first
    }
}

