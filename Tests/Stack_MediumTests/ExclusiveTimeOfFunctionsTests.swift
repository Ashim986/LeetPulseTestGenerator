private class Solution {
    func findExclusiveTimes(_ n: Int, _ logs: [String]) -> [Int] {
        var stack: [Int] = []
        var exclusiveTimes: [Int] = Array(repeating: 0, count: n)
        var prevTime: Int = 0

        for log in logs {
            let components = log.components(separatedBy: ":")
            let functionId = Int(components[0])!
            let eventType = components[1]
            let timestamp = Int(components[2])!

            if eventType == "start" {
                if !stack.isEmpty {
                    exclusiveTimes[stack.last!] += timestamp - prevTime
                }
                stack.append(functionId)
            } else {
                exclusiveTimes[stack.popLast()!] += timestamp - prevTime + 1
            }
            prevTime = timestamp
        }

        return exclusiveTimes
    }
}

