enum LC_check_if_word_is_valid_after_substitutions {
    private class Solution {
        func isValid(word: String) -> Bool {
          let pattern = "ab"
          var stack: [Character] = []
          for char in word {
            if char == "b" && !stack.isEmpty && stack.last == "a" {
              stack.popLast()
            } else {
              stack.append(char)
            }
          }
          return stack.count == 0 || (stack.count == 1 && stack[0] == "c")
        }
    }

