private class Solution {
    func validateStackSequences(pushed: [Int], popped: [Int]) -> Bool {
        var stack: [Int] = []
        var popIndex = 0
        for pushValue in pushed {
            stack.append(pushValue)
            while !stack.isEmpty, stack.last == popped[popIndex] {
                stack.removeLast()
                popIndex += 1
            }
        }
        return stack.isEmpty
    }
}

