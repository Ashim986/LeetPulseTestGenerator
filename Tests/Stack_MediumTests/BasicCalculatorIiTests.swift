private class Solution {
    func calculate(_ s: String) -> Int {
        var stack: [Int] = []
        var num = 0, op: Character = "+"
        let s = s + "+"
        for char in s {
            if char.isNumber {
                num = num * 10 + Int(String(char))!
            } else if char != " " {
                switch op {
                case "+": stack.append(num)
                case "-": stack.append(-num)
                case "*": stack.append(stack.removeLast() * num)
                case "/": stack.append(stack.removeLast() / num)
                default: break
                }
                op = char; num = 0
            }
        }
        return stack.reduce(0, +)
    }
}

