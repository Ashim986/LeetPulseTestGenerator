private class Solution {
    func robotSim(commands: [Int]) -> [Int] {
        var x = 0
        var y = 0
        var dx = 0
        var dy = 1
        var hashTable: [Int: (Int, Int)] = [:]
        for (i, command) in commands.enumerated() {
            if command == -1 {
                x += dx
                y += dy
            } else if command == -2 {
                x -= dx
                y -= dy
            } else {
                if command % 2 == 1 {
                    dy += command
                } else {
                    dx += command
                }
            }
            hashTable[i] = (x, y)
        }
        return [x, y]
    }
}

