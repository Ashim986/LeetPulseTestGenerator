enum LC_most_stones_removed_with_same_row_or_column {
    private class Solution {
        func removeStones(_ stones: [[Int]]) -> Int {
            var parent: [Int: Int] = [:]
            var rank: [Int: Int] = [:]

            func find(_ x: Int) -> Int {
                if parent[x] == nil {
                    parent[x] = x
                    rank[x] = 0
                }
                if parent[x] == x { return x }
                parent[x] = find(parent[x] ?? x)
                return parent[x] ?? x
            }

            func union(_ a: Int, _ b: Int) {
                let ra = find(a)
                let rb = find(b)
                if ra == rb { return }
                let rka = rank[ra, default: 0]
                let rkb = rank[rb, default: 0]
                if rka < rkb {
                    parent[ra] = rb
                } else if rka > rkb {
                    parent[rb] = ra
                } else {
                    parent[rb] = ra
                    rank[ra] = rka + 1
                }
            }

            let offset = 100_000
            for stone in stones {
                let r = stone[0]
                let c = stone[1] + offset
                union(r, c)
            }

            var roots = Set<Int>()
            for stone in stones {
                roots.insert(find(stone[0]))
            }
            return stones.count - roots.count
        }
    }

