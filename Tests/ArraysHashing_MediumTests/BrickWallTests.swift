enum LC_brick_wall {
    private class Solution {
        func leastBricks(_ wall: [[Int]]) -> Int {
            var edges: [Int: Int] = [:]
            var maxWidth: Int = 0
            for row in wall {
                maxWidth += row[0]
            }
            maxWidth -= 1
            for row in wall {
                var curEdge: Int = 0
                for i in 0..<row.count - 1 {
                    curEdge += row[i]
                    edges[curEdge, default: 0] += 1
                }
            }
            var maxCount: Int = 0
            for (_, count) in edges {
                maxCount = max(maxCount, count)
            }
            return wall.count - maxCount
        }
    }

