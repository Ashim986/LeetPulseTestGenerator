enum LC_distribute_coins_in_binary_tree {
    /**
     * Definition for a binary tree node.
     * 
     */
    private class Solution {
        var moves = 0

        func distributeCoins(_ root: TreeNode?) -> Int {
            dfs(root)
            return moves
        }

        func dfs(_ node: TreeNode?) -> Int {
            if node == nil {
                return 0
            }
            let leftSurplus = dfs(node!.left)
            let rightSurplus = dfs(node!.right)
            moves += abs(leftSurplus) + abs(rightSurplus)
            return node!.val + leftSurplus + rightSurplus - 1
        }
    }

