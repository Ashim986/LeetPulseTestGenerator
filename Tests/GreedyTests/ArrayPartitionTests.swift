private class Solution {
    func arrayPairSum(_ nums: [Int]) -> Int {
        var numsCopy = nums
        numsCopy.sort()
        var sum = 0
        for i in stride(from: 0, to: numsCopy.count, by: 2) {
            sum += min(numsCopy[i], numsCopy[i+1])
        }
        return sum
    }
}

