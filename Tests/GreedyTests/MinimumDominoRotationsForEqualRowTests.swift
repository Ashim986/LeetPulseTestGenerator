private class Solution {
    func minDominoRotations(_ A: [Int], _ B: [Int]) -> Int {
        let n = A.count
        func check(x: Int) -> Int {
            var rotationsA = 0
            var rotationsB = 0
            for i in 0..<n {
                if A[i] != x && B[i] != x {
                    return -1
                } else if A[i] != x {
                    rotationsA += 1
                } else if B[i] != x {
                    rotationsB += 1
                }
            }
            return min(rotationsA, rotationsB)
        }
        let rotationsA = check(x: A[0])
        let rotationsB = check(x: B[0])
        if rotationsA == -1 && rotationsB == -1 {
            return -1
        }
        if rotationsA == -1 {
            return rotationsB
        }
        if rotationsB == -1 {
            return rotationsA
        }
        return min(rotationsA, rotationsB)
    }
}

