private class Solution {
    func largestSumAfterKNegations(_ nums: [Int], _ k: Int) -> Int {
        var arr = nums.sorted()
        for i in 0..<min(arr.count, k) {
            arr[i] = -arr[i]
        }
        return arr.reduce(0, +)
    }
}

