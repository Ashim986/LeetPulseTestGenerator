private class Solution {
    func minIncrementForUnique(_ A: [Int]) -> Int { 
        let sorted = A.sorted() 
        var maxSeen = -1 
        var result = 0 
        for num in sorted { 
            if num <= maxSeen { 
                let increment = maxSeen - num + 1 
                result += increment 
                maxSeen += 1 
            } else { 
                maxSeen = num 
            } 
        } 
        return result 
    }
}

