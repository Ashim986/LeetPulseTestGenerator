private class Solution {
    func triangleNumber(_ nums: [Int]) -> Int {
        let sortedNums = nums.sorted()
        var count = 0
        for i in 0..<sortedNums.count {
            var left = 0
            var right = i - 1
            while left < right {
                if sortedNums[left] + sortedNums[right] > sortedNums[i] {
                    count += right - left
                    left += 1
                } else {
                    right -= 1
                }
            }
        }
        return count
    }
}

