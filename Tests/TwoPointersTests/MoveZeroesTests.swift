private class Solution {
    func moveZeroes(_ nums: inout [Int]) {
        var slow = 0

        for fast in 0..<nums.count {
            if nums[fast] != 0 {
                // Swap only if positions differ (avoid self-swap)
                if slow != fast {
                    nums.swapAt(slow, fast)
                }
                slow += 1
            }
        }
    }
}

