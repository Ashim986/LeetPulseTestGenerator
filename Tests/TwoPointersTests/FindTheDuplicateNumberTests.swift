private class Solution {
    func findDuplicate(_ nums: [Int]) -> Int {
        var lo = 1
        var hi = nums.count - 1

        while lo < hi {
            let mid = lo + (hi - lo) / 2
            let count = nums.filter { $0 <= mid }.count

            if count > mid {
                hi = mid  // Duplicate in lower half
            } else {
                lo = mid + 1  // Duplicate in upper half
            }
        }

        return lo
    }
}

