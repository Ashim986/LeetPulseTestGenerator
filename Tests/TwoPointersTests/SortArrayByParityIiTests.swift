private class Solution {
    func sortArrayByParityII(_ nums: [Int]) -> [Int] {
        var nums = nums
        for i in stride(from: 0, to: nums.count, by: 2) {
            if nums[i] % 2 == 1 {
                for j in stride(from: i + 1, to: nums.count, by: 2) {
                    if nums[j] % 2 == 0 {
                        nums.swapAt(i, j)
                        break
                    }
                }
            }
        }
        return nums
    }
}

