private class Solution {
    func sortArrayByParity(_ A: [Int]) -> [Int] {
        var arr = A
        var evenIndex = 0
        for (index, value) in arr.enumerated() {
            if value % 2 == 0 {
                let temp = arr[evenIndex]
                arr[evenIndex] = arr[index]
                arr[index] = temp
                evenIndex += 1
            }
        }
        return arr
    }
}

