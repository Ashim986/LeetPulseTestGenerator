private class Solution {
    func advantageCount(_ A: [Int], _ B: [Int]) -> [Int] {
        let n = A.count
        var res: [Int] = Array(repeating: 0, count: n)
        let a = A.sorted()
        let b = B.sorted { $0 > $1 }
        var left = 0
        var right = n - 1
        for bIndex in 0..<n {
            if a[right] > b[bIndex] {
                res[bIndex] = a[right]
                right -= 1
            } else {
                res[bIndex] = a[left]
                left += 1
            }
        }
        return res
    }
}

