private class Solution {
    func rangeBitwiseAnd(_ m: Int, _ n: Int) -> Int {
        var mCopy = m
        var nCopy = n
        var shift = 0
        while mCopy != nCopy {
            mCopy >>= 1
            nCopy >>= 1
            shift += 1
        }
        return mCopy << shift
    }
}

