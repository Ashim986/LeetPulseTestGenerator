private class Solution {
    func nextGreatestLetter(_ letters: [Character], _ target: Character) -> Character {
        let n = letters.count
        var left = 0
        var right = n
        while left < right {
            let mid = left + (right - left) / 2
            if letters[mid] <= target {
                left = mid + 1
            } else {
                right = mid
            }
        }
        return left == n ? letters[0] : letters[left]
    }
}

