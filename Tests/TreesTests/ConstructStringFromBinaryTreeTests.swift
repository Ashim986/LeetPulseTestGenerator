private class Solution {
    func tree2str(_ t: TreeNode?) -> String {
        guard let t = t else { return "" }
        var result = String(t.val)
        if t.left == nil && t.right == nil {
            return result
        }
        result += "("
        if let l = t.left {
            result += tree2str(l)
        }
        if t.left != nil && t.right != nil {
            result += ")("
        }
        if let r = t.right {
            result += tree2str(r)
        }
        result += ")"
        return result
    }
}

