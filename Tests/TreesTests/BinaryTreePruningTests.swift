private class Solution {
    func pruneTree(_ root: TreeNode?) -> TreeNode? {
        func prune(_ node: TreeNode?) -> TreeNode? {
            if node == nil {
                return nil
            }
            node?.left = prune(node?.left)
            node?.right = prune(node?.right)
            if node?.val == 0, node?.left == nil, node?.right == nil {
                return nil
            }
            return node
        }
        return prune(root)
    }
}

