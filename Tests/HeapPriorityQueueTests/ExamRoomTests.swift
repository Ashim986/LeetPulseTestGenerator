private class Solution {
    class ExamRoom {
        let n: Int
        var occupied: Set<Int>
        var pq: [(Int, Int)] = []
        let INF = Int.max

        init(_ n: Int) {
            self.n = n
            occupied = Set()
        }

        func seat() -> Int {
            if occupied.isEmpty {
                occupied.insert(0)
                return 0
            }
            var maxDist = -1
            var seat = -1
            for i in occupied {
                let dist = max(i, n - 1 - i)
                if dist > maxDist {
                    maxDist = dist
                    seat = i
                }
            }
            for i in occupied {
                let left = i - 1
                let right = i + 1
                if left >= 0 && occupied.contains(left) {
                    continue
                }
                if right < n && occupied.contains(right) {
                    continue
                }
                let dist = max(left >= 0 ? i - left : i, right < n ? right - i : n - 1 - i)
                if dist > maxDist {
                    maxDist = dist
                    seat = left >= 0 && left < n ? (i + left) / 2 : right < n ? (i + right) / 2 : i
                }
            }
            occupied.insert(seat)
            return seat
        }

        func leave(_ p: Int) {
            if occupied.contains(p) {
                occupied.remove(p)
            }
            var index = 0
            while index < pq.count {
                if pq[index].1 == p {
                    pq.remove(at: index)
                    break
                }
                index += 1
            }
        }
    }
}

