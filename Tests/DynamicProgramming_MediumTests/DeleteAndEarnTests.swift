enum LC_delete_and_earn {
    private class Solution {
        func deleteAndEarn(_ nums: [Int]) -> Int {
            let maxNum = nums.max()!
            var freq = Array(repeating: 0, count: maxNum + 1)
            for num in nums {
                freq[num] += 1
            }
            var dp = Array(repeating: 0, count: maxNum + 1)
            dp[1] = freq[1]
            for i in 2...maxNum {
                dp[i] = max(dp[i - 1], dp[i - 2] + freq[i] * i)
            }
            return dp[maxNum]
        }
    }

