private class Solution {
    func knightDialer(_ N: Int) -> Int { 
      let moves = [ 
        0: [4, 6], 
        1: [6, 8], 
        2: [7, 9], 
        3: [4, 8], 
        4: [0, 3, 9], 
        5: [], 
        6: [0, 1, 7], 
        7: [2, 6], 
        8: [1, 3], 
        9: [2, 4] 
      ]; 
      var dp = [Int](repeating: 1, count: 10) 
      if N == 1 { 
        return 10 
      } 
      for _ in 2...N { 
        var temp = [Int](repeating: 0, count: 10) 
        for i in 0...9 { 
          for j in moves[i]! { 
            temp[i] += dp[j] 
          } 
        } 
        dp = temp 
      } 
      return dp.reduce(0, +) 
    }
}

