private class Solution {
    func numDecodings(_ s: String) -> Int {
        let chars = Array(s)
        let n = chars.count
        if chars[0] == "0" { return 0 }

        var prev2 = 1  // dp[i-2]
        var prev1 = 1  // dp[i-1]

        for i in 1..<n {
            var curr = 0
            let oneDigit = Int(String(chars[i]))!
            let twoDigit = Int(String(chars[i-1...i]))!

            if oneDigit >= 1 { curr += prev1 }
            if twoDigit >= 10 && twoDigit <= 26 { curr += prev2 }

            prev2 = prev1
            prev1 = curr
        }

        return prev1
    }
}

