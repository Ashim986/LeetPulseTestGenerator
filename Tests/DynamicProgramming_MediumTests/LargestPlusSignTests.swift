private class Solution {
    func orderOfLargestPlusSign(_ n: Int, _ mines: [Int]) -> Int {
        let set = Set(mines)
        var res = 0
        var up = Array(repeating: 0, count: n*n)
        var down = Array(repeating: 0, count: n*n)
        var left = Array(repeating: 0, count: n*n)
        var right = Array(repeating: 0, count: n*n)
        for i in 0..<n {
            for j in 0..<n {
                if !set.contains(i*n+j) {
                    up[i*n+j] = (i == 0 ? 0 : up[(i-1)*n+j]) + 1
                    left[i*n+j] = (j == 0 ? 0 : left[i*n+j-1]) + 1
                }
            }
        }
        for i in stride(from: n-1, to: -1, by: -1) {
            for j in stride(from: n-1, to: -1, by: -1) {
                if !set.contains(i*n+j) {
                    down[i*n+j] = (i == n-1 ? 0 : down[(i+1)*n+j]) + 1
                    right[i*n+j] = (j == n-1 ? 0 : right[i*n+j+1]) + 1
                }
            }
        }
        for i in 0..<n {
            for j in 0..<n {
                if !set.contains(i*n+j) {
                    res = max(res, min(min(up[i*n+j], down[i*n+j]), min(left[i*n+j], right[i*n+j])))
                }
            }
        }
        return res
    }
}

