private class Solution {
    func rob(_ nums: [Int]) -> Int {
        var prev2 = 0  // max from 2 houses back
        var prev1 = 0  // max from previous house

        for num in nums {
            let curr = max(prev1, prev2 + num)
            prev2 = prev1
            prev1 = curr
        }

        return prev1
    }
}

