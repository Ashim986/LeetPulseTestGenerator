enum LC_unique_substrings_in_wraparound_string {
    private class Solution {
        func findSubstringInWraproundString(_ p: String) -> Int {
            let n = p.count
            var dp = Array(repeating: 0, count: 26)
            var maxLength = 0
            for (i, char) in p.enumerated() {
                let currCharIndex = char.asciiValue! - 97
                if i == 0 || (currCharIndex + 26 - (p[p.index(p.startIndex, offsetBy: i - 1)].asciiValue! - 97)) % 26 != 1 {
                    maxLength = 0
                }
                maxLength += 1
                dp[Int(currCharIndex)] = max(dp[Int(currCharIndex)], maxLength)
            }
            return dp.reduce(0) { $0 + $1 }
        }
    }

