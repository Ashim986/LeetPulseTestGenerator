enum LC_domino_and_tromino_tiling {
    private class Solution {
        func numTilings(n: Int) -> Int {
            let MOD = 1000000007
            if n == 1 {
                return 0
            }
            if n == 2 {
                return 1
            }
            if n == 3 {
                return 2
            }
            var dp = Array(repeating: 0, count: n + 1)
            dp[1] = 0
            dp[2] = 1
            dp[3] = 2
            for i in 4...n {
                dp[i] = (2 * dp[i-1]) + dp[i-3]
                dp[i] %= MOD
            }
            return dp[n]
        }
    }

