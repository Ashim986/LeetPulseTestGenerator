private class Solution {
    func maxScoreSightseeingPair(_ A: [Int]) -> Int {
        var res = 0, maxScore = A[0] + 0
        for j in 1..<A.count {
            res = max(res, maxScore + A[j] - j)
            maxScore = max(maxScore, A[j] + j)
        }
        return res
    }
}

