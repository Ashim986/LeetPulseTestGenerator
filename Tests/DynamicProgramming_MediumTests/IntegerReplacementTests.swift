private class Solution {
    func integerReplacement(n: Int) -> Int {
      var number = Int64(n)
      var count = 0
      while number != 1 {
        if number % 2 == 0 {
          number /= 2
        } else if number % 4 == 1 || number == 3 {
          number += 1
        } else {
          number -= 1
        }
        count += 1
      }
      return count
    }
}

