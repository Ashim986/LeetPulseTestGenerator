private class Solution {
    func lengthOfLIS(_ nums: [Int]) -> Int {
        var tails: [Int] = []

        for num in nums {
            var left = 0, right = tails.count
            while left < right {
                let mid = (left + right) / 2
                if tails[mid] < num {
                    left = mid + 1
                } else {
                    right = mid
                }
            }
            if left == tails.count {
                tails.append(num)
            } else {
                tails[left] = num
            }
        }

        return tails.count
    }
}

