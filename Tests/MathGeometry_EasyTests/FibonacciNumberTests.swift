private class Solution {
    func fib(_ n: Int) -> Int {
        var memo: [Int: Int] = [0: 0, 1: 1]
        func fibonacci(_ n: Int) -> Int {
            if memo[n] != nil { return memo[n]! }
            let result = fibonacci(n-1) + fibonacci(n-2)
            memo[n] = result
            return result
        }
        return fibonacci(n)
    }
}

