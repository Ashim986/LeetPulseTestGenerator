private class Solution {
    func isPowerOfFour(_ n: Int) -> Bool {
      if n <= 0 {
        return false
      }
      let binary = String(n, radix: 2)
      let countOnes = binary.filter { $0 == "1" }.count
      let countZeros = binary.filter { $0 == "0" }.count
      if countOnes != 1 {
        return false
      }
      return countZeros % 2 == 0
    }
}

