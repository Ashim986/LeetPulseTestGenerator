private class Solution {
    func romanToInt(_ s: String) -> Int {
        let values: [Character: Int] = [
            "I": 1, "V": 5, "X": 10, "L": 50,
            "C": 100, "D": 500, "M": 1000
        ]

        let chars = Array(s)
        var result = 0

        for i in 0..<chars.count {
            let curr = values[chars[i], default: 0]
            let next = i + 1 < chars.count ? values[chars[i + 1], default: 0] : 0

            if curr < next {
                result -= curr
            } else {
                result += curr
            }
        }

        return result
    }
}

