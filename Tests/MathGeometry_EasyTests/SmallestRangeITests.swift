enum LC_smallest_range_i {
    private class Solution {
        func smallestRange(_ nums: [[Int]], _ k: Int) -> [Int] {
            let n = nums.count
            let minHeap = MinHeap()
            for i in 0..<n {
                minHeap.insert((nums[i][0], i, 0))
            }
            var maxVal = minHeap.peek()?.0 ?? 0
            var minRange = [maxVal, maxVal]
            while true {
                let (val, array, index) = minHeap.extractMin()!
                if val > minRange[0] {
                    minRange = [minRange[0], val]
                }
                if index + 1 < nums[array].count {
                    minHeap.insert((nums[array][index + 1], array, index + 1))
                    maxVal = max(maxVal, nums[array][index + 1])
                } else {
                    break
                }
            }
            let minRangeSize = maxVal - minRange[0]
            if minRangeSize <= k {
                return minRange
            } else {
                return [minRange[0], minRange[0] + k]
            }
        }

        class MinHeap {
            var heap: [(Int, Int, Int)] = []

            func insert(_ val: (Int, Int, Int)) {
                heap.append(val)
                var index = heap.count - 1
                while index > 0 {
                    let parentIndex = (index - 1) / 2
                    if heap[parentIndex].0 > heap[index].0 {
                        heap.swapAt(parentIndex, index)
                        index = parentIndex
                    } else {
                        break
                    }
                }
            }

            func extractMin() -> (Int, Int, Int)? {
                if heap.isEmpty {
                    return nil
                }
                if heap.count == 1 {
                    return heap.removeFirst()
                }
                let min = heap[0]
                heap[0] = heap.removeLast()
                var index = 0
                while index * 2 + 1 < heap.count {
                    var smallest = index * 2 + 1
                    if index * 2 + 2 < heap.count && heap[index * 2 + 2].0 < heap[smallest].0 {
                        smallest = index * 2 + 2
                    }
                    if heap[smallest].0 < heap[index].0 {
                        heap.swapAt(smallest, index)
                        index = smallest
                    } else {
                        break
                    }
                }
                return min
            }

            func peek() -> (Int, Int, Int)? {
                if heap.isEmpty {
                    return nil
                }
                return heap[0]
            }
        }
    }

