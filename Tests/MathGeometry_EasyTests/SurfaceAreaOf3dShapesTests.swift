private class Solution {
    func surfaceArea(grid: [[Int]]) -> Int {
      var area = 0
      let rows = grid.count
      let cols = grid[0].count

      for i in 0..<rows {
        for j in 0..<cols {
          if grid[i][j] > 0 {
            area += 4 * grid[i][j] + 2
            if i > 0 {
              area -= 2 * min(grid[i][j], grid[i - 1][j])
            }
            if j > 0 {
              area -= 2 * min(grid[i][j], grid[i][j - 1])
            }
          }
        }
      }
      return area
    }
}

