private class Solution {
    func divisorGame(_ N: Int) -> Bool { 
        var dp: [Bool] = [Bool](repeating: false, count: N + 1) 
        dp[0] = false 
        dp[1] = false 
        for i in 2...N { 
            for j in 1...i { 
                if i % j == 0 && !dp[i - j] { 
                    dp[i] = true 
                    break 
                } 
            } 
        } 
        return dp[N] 
    }
}

