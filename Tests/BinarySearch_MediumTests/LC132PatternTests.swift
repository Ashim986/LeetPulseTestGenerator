private class Solution {
    func find132pattern(_ nums: [Int]) -> Bool {
        var stack: [Int] = []
        var s3 = Int.min
        for num in nums.reversed() {
            if num < s3 {
                return true
            }
            while !stack.isEmpty, stack.last! < num {
                s3 = stack.removeLast()
            }
            stack.append(num)
        }
        return false
    }
}

