enum LC_sqrt_x {
    private class Solution {
        func mySqrt(_ x: Int) -> Int {
            if x < 2 { return x }
            var left = 1, right = x / 2, result = 0
            while left <= right {
                let mid = left + (right - left) / 2
                if mid <= x / mid { result = mid; left = mid + 1 }
                else { right = mid - 1 }
            }
            return result
        }
    }

