private class Solution {
    func longestOnes(_ nums: [Int], _ k: Int) -> Int {
        var maxLength = 0
        var left = 0
        var zeros = 0
        for right in 0..<nums.count {
            if nums[right] == 0 {
                zeros += 1
            }
            while zeros > k {
                if nums[left] == 0 {
                    zeros -= 1
                }
                left += 1
            }
            maxLength = max(maxLength, right - left + 1)
        }
        return maxLength
    }
}

