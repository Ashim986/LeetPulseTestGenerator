import Foundation
import Testing
@testable import LeetCodeHelpers

private class Solution {
    func findTheDifference(_ s: String, _ t: String) -> Character {
        var result = 0
        for c in s {
            result = result ^ Int(c.asciiValue ?? 0)
        }
        for c in t {
            result = result ^ Int(c.asciiValue ?? 0)
        }
        return Character(UnicodeScalar(UInt32(result)) ?? UnicodeScalar(0))
    }
}

