private class Solution {
    class TrieNode {
        var children = [Int: TrieNode]()
        var EndOfNumber = false
    }

    func findMaximumXOR(_ nums: [Int]) -> Int {
        var root = TrieNode()
        for num in nums {
            var node = root
            var tempNum = num
            for _ in 1...31 {
                let bit = tempNum & 1
                tempNum >>= 1
                if node.children[bit] == nil {
                    node.children[bit] = TrieNode()
                }
                node = node.children[bit]!
            }
            node.EndOfNumber = true
        }

        func find(num: Int, node: TrieNode) -> Int {
            var result = 0
            var tempNum = num
            var node = node
            for i in 1...31 {
                let bit = tempNum & 1
                tempNum >>= 1
                let oppositeBit = 1 - bit
                if let nextNode = node.children[oppositeBit] {
                    result += (1 << (31 - i))
                    node = nextNode
                } else if let nextNode = node.children[bit] {
                    node = nextNode
                }
            }
            return result
        }

        var max = 0
        for num in nums {
            max = Swift.max(max, find(num: num, node: root))
        }
        return max
    }
}

