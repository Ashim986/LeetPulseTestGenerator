private class Solution {
    func maxProduct(words: [String]) -> Int {
        let n = words.count
        var masks = [Int]()
        var maxProduct = 0
        for word in words {
            var mask = 0
            for char in word {
                let bit = 1 << (UnicodeScalar(String(char))!.value - 97)
                mask |= Int(bit)
            }
            masks.append(mask)
        }
        for i in 0..<n {
            for j in (i+1)..<n {
                if (masks[i] & masks[j]) == 0 {
                    maxProduct = max(maxProduct, words[i].count * words[j].count)
                }
            }
        }
        return maxProduct
    }
}

