private class Solution {
    func binaryGap(N: Int) -> Int {
        var n = N
        var maxGap = 0
        var prevOne = -1
        var position = 0
        while n > 0 {
            if n % 2 == 1 {
                if prevOne != -1 {
                    maxGap = max(maxGap, position - prevOne)
                }
                prevOne = position
            }
            n /= 2
            position += 1
        }
        return maxGap
    }
}

