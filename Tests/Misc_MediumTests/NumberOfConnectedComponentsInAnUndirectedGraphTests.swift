enum LC_number_of_connected_components_in_an_undirected_graph {
    private class Solution {
        func countComponents(_ n: Int, _ edges: [[Int]]) -> Int {
            var parent = Array(0..<n)
            var rank = [Int](repeating: 0, count: n)
            var components = n

            func find(_ x: Int) -> Int {
                if parent[x] != x {
                    parent[x] = find(parent[x])
                }
                return parent[x]
            }

            func union(_ x: Int, _ y: Int) {
                let px = find(x), py = find(y)
                if px == py { return }
                if rank[px] < rank[py] {
                    parent[px] = py
                } else if rank[px] > rank[py] {
                    parent[py] = px
                } else {
                    parent[py] = px
                    rank[px] += 1
                }
                components -= 1
            }

            for edge in edges {
                union(edge[0], edge[1])
            }

            return components
        }
    }

