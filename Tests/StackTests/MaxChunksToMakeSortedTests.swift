private class Solution {
    func maxChunksToSorted(_ arr: [Int]) -> Int { 
        var max = 0, count = 0 
        for (i, x) in arr.enumerated() { 
            max = Swift.max(max, x) 
            if max == i { 
                count += 1 
            } 
        } 
        return count 
    }
}

