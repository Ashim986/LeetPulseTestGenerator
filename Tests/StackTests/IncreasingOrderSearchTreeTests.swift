private class Solution { 
    var prev: TreeNode? 
    func increasingBST(_ root: TreeNode?) -> TreeNode? { 
        let dummy = TreeNode(0) 
        prev = dummy 
        inorder(root) 
        return dummy.right 
    } 
    func inorder(_ node: TreeNode?) { 
        if node == nil { return } 
        inorder(node?.left) 
        node?.left = nil 
        prev?.right = node 
        prev = node 
        inorder(node?.right) 
    } 
}

