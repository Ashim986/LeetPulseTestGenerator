private class Solution {
    func findRestaurant(list1: [String], list2: [String]) -> [Int] {
        var dict: [String: Int] = [:]
        var minSum = Int.max
        var result: [Int] = []
        for i in 0..<list1.count {
            dict[list1[i]] = i
        }
        for j in 0..<list2.count {
            if let i = dict[list2[j]] {
                let sum = i + j
                if sum < minSum {
                    minSum = sum
                    result = [i, j]
                }
                else if sum == minSum {
                    result.append(i)
                    result.append(j)
                }
            }
        }
        return result
    }
}

