private class Solution {
    func isToeplitzMatrix(_ matrix: [[Int]]) -> Bool {
      let rows = matrix.count
      let cols = matrix[0].count
      for row in 1..<rows {
        for col in 1..<cols {
          if matrix[row][col] != matrix[row-1][col-1] {
            return false
          }
        }
      }
      return true
    }
}

