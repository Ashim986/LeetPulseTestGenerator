private class Solution {
    func findWords(_ words: [String]) -> [String] {
        let rows = [Set("qwertyuiop"), Set("asdfghjkl"), Set("zxcvbnm")]
        var result: [String] = []
        for word in words {
            let lowerWord = word.lowercased()
            let wordSet = Set(lowerWord)
            for row in rows {
                if row.isSuperset(of: wordSet) {
                    result.append(word)
                    break
                }
            }
        }
        return result
    }
}

