private class Solution {
    func findJudge(_ n: Int, _ trust: [[Int]]) -> Int {
        var trustScores: [Int] = Array(repeating: 0, count: n + 1)
        for edge in trust {
            trustScores[edge[0]] -= 1
            trustScores[edge[1]] += 1
        }
        for person in 1...n {
            if trustScores[person] == n - 1 {
                return person
            }
        }
        return -1
    }
}

