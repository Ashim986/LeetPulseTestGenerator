private class Solution {
    func validIPAddress(_ IP: String) -> String {
        var urlComponents = URLComponents(string: "http://(IP)")
        var host = urlComponents?.host

        if host?.contains(":") == true {
            if host?.components(separatedBy: ":").count == 8 {
                for part in (host?.components(separatedBy: ":") ?? []) {
                    if part.count > 4 {
                        return "Neither"
                    }

                    for char in part {
                        if !(char >= "0" && char <= "9" || char >= "a" && char <= "f" || char >= "A" && char <= "F") {
                            return "Neither"
                        }
                    }
                }
                return "IPv6"
            } else {
                return "Neither"
            }
        } else if host?.contains(".") == true {
            if (host?.components(separatedBy: ".") ?? []).count == 4 {
                for part in (host?.components(separatedBy: ".") ?? []) {
                    if part.count > 1 && part.first == "0" {
                        return "Neither"
                    }

                    if let number = Int(part) {
                        if number < 0 || number > 255 {
                            return "Neither"
                        }
                    } else {
                        return "Neither"
                    }
                }
                return "IPv4"
            } else {
                return "Neither"
            }
        } else {
            return "Neither"
        }
    }
}

