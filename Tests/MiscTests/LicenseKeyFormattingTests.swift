import Foundation
import Testing
@testable import LeetCodeHelpers

private class Solution {
    func licenseKeyFormatting(_ S: String, _ K: Int) -> String { let s = S.replacingOccurrences(of: "-", with: "").uppercased(); var result = ""; for (index, ch) in s.enumerated() { if index == s.count - K { result.append(String(ch)) } else if (s.count - index) % K == 0 { result.append("-"); result.append(String(ch)) } else { result.append(String(ch)) } }; return result }
}

