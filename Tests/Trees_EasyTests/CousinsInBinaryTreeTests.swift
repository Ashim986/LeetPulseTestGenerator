private class Solution {
    func isCousins(_ root: TreeNode?, _ x: Int, _ y: Int) -> Bool {
        var queue: [(TreeNode, Int, Int?)] = [(root!, 0, nil)]
        var dict: [Int: (Int, Int?)] = [:]

        while !queue.isEmpty {
            let (node, depth, parent) = queue.removeFirst()
            dict[node.val] = (depth, parent)

            if node.left != nil {
                queue.append((node.left!, depth + 1, node.val))
            }
            if node.right != nil {
                queue.append((node.right!, depth + 1, node.val))
            }
        }

        let (xDepth, xParent) = dict[x]!
        let (yDepth, yParent) = dict[y]!

        return xDepth == yDepth && xParent != yParent
    }
}

