private class Solution {
    func calPoints(ops: [String]) -> Int {
        var stack: [Int] = []
        for op in ops {
            if let num = Int(op) {
                stack.append(num)
            }
            else if op == "C" {
                stack.removeLast()
            }
            else if op == "D" {
                stack.append(stack.last! * 2)
            }
        }
        return stack.reduce(0, +)
    }
}

