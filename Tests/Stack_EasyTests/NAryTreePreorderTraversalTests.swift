private typealias Node = NAryNode

private class Solution {
    func preorder(_ root: Node?) -> [Int] {
        var result = [Int]()
        var stack = [Node]()
        if let root = root {
            stack.append(root)
        }
        while !stack.isEmpty {
            let node = stack.removeLast()
            result.append(node.val)
            for child in node.children.reversed() {
                stack.append(child)
            }
        }
        return result
    }
}

