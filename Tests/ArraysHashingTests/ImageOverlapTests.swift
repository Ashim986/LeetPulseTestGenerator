private class Solution {
    func largestOverlap(img1: [[Int]], img2: [[Int]]) -> Int {
        let m = img1.count
        let n = img1[0].count
        var maxOverlap = 0
        var count = [String: Int]()
        for x in 0..<m {
            for y in 0..<n {
                if img1[x][y] == 1 {
                    for dx in -m+1...m-1 {
                        for dy in -n+1...n-1 {
                            let key = "(dx),(dy)"
                            count[key, default: 0] += 1
                        }
                    }
                }
            }
        }
        for x in 0..<m {
            for y in 0..<n {
                if img2[x][y] == 1 {
                    for dx in -m+1...m-1 {
                        for dy in -n+1...n-1 {
                            let key = "(dx),(dy)"
                            maxOverlap = max(maxOverlap, count[key, default: 0])
                        }
                    }
                }
            }
        }
        return maxOverlap
    }
}

