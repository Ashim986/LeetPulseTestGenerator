private class Solution {
    func findMaxLength(_ nums: [Int]) -> Int {
        let n = nums.count
        var maxLength = 0
        var count = 0
        var prefixSum: [Int: Int] = [0: -1]
        for i in 0..<n {
            if nums[i] == 0 {
                count -= 1
            } else {
                count += 1
            }
            if let j = prefixSum[count] {
                maxLength = max(maxLength, i - j)
            } else {
                prefixSum[count] = i
            }
        }
        return maxLength
    }
}

