import Foundation
import Testing
@testable import LeetCodeHelpers

private class Solution {
    func distributeCandies(_ candyType: [Int]) -> Int { 
        let types = Array(Set(candyType)) 
        let sisterCount = candyType.count / 2 
        return min(types.count, sisterCount) 
    }
}

