private class Solution {
    func longestConsecutive(_ nums: [Int]) -> Int {
        let numSet = Set(nums)
        var maxLength = 0

        for num in numSet {
            // Only start if this is beginning of sequence
            if !numSet.contains(num - 1) {
                var current = num
                var length = 1
                while numSet.contains(current + 1) {
                    current += 1
                    length += 1
                }
                maxLength = max(maxLength, length)
            }
        }

        return maxLength
    }
}

