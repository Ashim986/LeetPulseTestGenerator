private class Solution {
    class PeekingIterator {
        let iterator: IndexingIterator<[Int]>
        var current: Int?

        init(_ arr: [Int]) {
            iterator = arr.makeIterator()
        }

        func peek() -> Int {
            if current == nil {
                current = iterator.next()
            }
            return current!
        }

        func next() -> Int {
            if current == nil {
                current = iterator.next()
            }
            let temp = current
            current = nil
            return temp!
        }
    }
}

