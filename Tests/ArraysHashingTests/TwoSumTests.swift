private class Solution {
    func twoSum(_ nums: [Int], _ target: Int) -> [Int] {
        var valueToIndex: [Int: Int] = [:]
        for (i, num) in nums.enumerated() {
            let complement = target - num
            if let j = valueToIndex[complement] {
                return [j, i]
            }
            valueToIndex[num] = i
        }
        return []
    }
}

