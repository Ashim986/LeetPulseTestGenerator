private class Solution {
    func arrayNesting(_ nums: [Int]) -> Int {
        var maxLen = 0
        var visited: Set<Int> = Set()
        var n = nums.count

        for i in 0..<n {
            if !visited.contains(i) {
                var len = 0
                var j = i
                while !visited.contains(j) {
                    visited.insert(j)
                    len += 1
                    j = nums[j]
                }
                maxLen = max(maxLen, len)
            }
        }

        return maxLen
    }
}

