private class Solution {
    func thirdMax(_ nums: [Int]) -> Int {
        var max1 = Int.min
        var max2 = Int.min
        var max3 = Int.min
        for num in nums {
            if num > max1 {
                max3 = max2
                max2 = max1
                max1 = num
            } else if num > max2 && num != max1 {
                max3 = max2
                max2 = num
            } else if num > max3 && num != max1 && num != max2 {
                max3 = num
            }
        }
        if max3 == Int.min {
            return max1
        }
        return max3
    }
}

