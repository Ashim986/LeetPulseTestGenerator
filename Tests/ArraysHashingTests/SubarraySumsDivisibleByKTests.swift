private class Solution {
    func subarraysDivByK(_ nums: [Int], _ k: Int) -> Int {
        let n = nums.count
        var count = 0
        var prefixSum = 0
        var sumCount: [Int: Int] = [0: 1]
        for num in nums {
            prefixSum = (prefixSum + num) % k
            if prefixSum < 0 {
                prefixSum += k
            }
            if let val = sumCount[prefixSum] {
                count += val
            }
            sumCount[prefixSum, default: 0] += 1
        }
        return count
    }
}

