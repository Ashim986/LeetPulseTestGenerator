private class Solution {
    func islandPerimeter(_ grid: [[Int]]) -> Int {
        let m = grid.count
        let n = grid[0].count
        var perimeter = 0
        for i in 0..<m {
            for j in 0..<n {
                if grid[i][j] == 1 {
                    let neighbors = [(i-1,j), (i+1,j), (i,j-1), (i,j+1)]
                    for (x, y) in neighbors {
                        if x < 0 || y < 0 || x >= m || y >= n || grid[x][y] == 0 {
                            perimeter += 1
                        }
                    }
                }
            }
        }
        return perimeter
    }
}

