private class Solution {
    func matrixReshape(_ mat: [[Int]], _ r: Int, _ c: Int) -> [[Int]] { 
        let numRows = mat.count 
        let numCols = mat[0].count 
        guard numRows * numCols == r * c else { return mat } 
        var flat: [Int] = [] 
        for i in 0..<numRows { 
            for j in 0..<numCols { 
                flat.append(mat[i][j]) 
            } 
        } 
        var result: [[Int]] = [] 
        for i in 0..<r { 
            result.append(Array(flat[(i*c)..<(i*c+c)])) 
        } 
        return result 
    }
}

