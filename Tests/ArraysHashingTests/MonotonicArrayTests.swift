private class Solution {
    func isMonotonic(_ nums: [Int]) -> Bool {
        var increasing = false
        var decreasing = false

        for i in 1..<nums.count {
            if nums[i] > nums[i-1] {
                increasing = true
            }
            if nums[i] < nums[i-1] {
                decreasing = true
            }
            if increasing && decreasing {
                return false
            }
        }

        return true
    }
}

