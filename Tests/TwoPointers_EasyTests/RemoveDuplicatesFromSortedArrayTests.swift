private class Solution {
    func removeDuplicates(_ nums: inout [Int]) -> Int {
        guard nums.count > 0 else { return 0 }

        var writeIndex = 0

        for i in 1..<nums.count {
            if nums[i] != nums[writeIndex] {
                writeIndex += 1
                nums[writeIndex] = nums[i]
            }
        }

        return writeIndex + 1
    }
}

