private class Solution {
    func reverseOnlyLetters(_ S: String) -> String {
        var s = Array(S)
        var left = 0
        var right = s.count - 1
        while left < right {
            if !isLetter(s[left]) {
                left += 1
            } else if !isLetter(s[right]) {
                right -= 1
            } else {
                let temp = s[left]
                s[left] = s[right]
                s[right] = temp
                left += 1
                right -= 1
            }
        }
        return String(s)
    }

    func isLetter(_ c: Character) -> Bool {
        return (c >= "A" && c <= "Z") || (c >= "a" && c <= "z")
    }
}

