enum LC_backspace_string_compare {
    private class Solution {
        func backspaceCompare(_ s: String, _ t: String) -> Bool {
            let sArray = Array(s), tArray = Array(t)
            var i = sArray.count - 1, j = tArray.count - 1
            var skipS = 0, skipT = 0
            while i >= 0 || j >= 0 {
                while i >= 0 {
                    if sArray[i] == "#" {
                        skipS += 1
                        i -= 1
                    }
                    else if skipS > 0 {
                        skipS -= 1
                        i -= 1
                    }
                    else {
                        break
                    }
                }
                while j >= 0 {
                    if tArray[j] == "#" {
                        skipT += 1
                        j -= 1
                    }
                    else if skipT > 0 {
                        skipT -= 1
                        j -= 1
                    }
                    else {
                        break
                    }
                }
                if i >= 0 && j >= 0 && sArray[i] != tArray[j] {
                    return false
                }
                if (i >= 0) != (j >= 0) {
                    return false
                }
                i -= 1
                j -= 1
            }
            return true
        }
    }

