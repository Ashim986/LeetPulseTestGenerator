private class Solution {
    func minMovesToEqualArrayElementsII(_ nums: [Int]) -> Int {
        let sortedNums = nums.sorted()
        let median = sortedNums[sortedNums.count / 2]
        let totalMoves = sortedNums.reduce(0, { $0 + abs($1 - median) })

        return totalMoves
    }
}

