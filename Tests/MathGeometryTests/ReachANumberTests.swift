private class Solution {
    func reachNumber(target: Int) -> Int {
        let target = abs(target)
        var step = 0
        var sum = 0
        while sum < target {
            step += 1
            sum += step
        }
        while (sum - target) % 2 != 0 {
            step += 1
            sum += step
        }
        return step
    }
}

