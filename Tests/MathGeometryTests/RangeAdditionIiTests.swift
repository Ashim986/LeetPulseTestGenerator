private class Solution {
    func maxCount(_ m: Int, _ n: Int, _ ops: [[Int]]) -> Int {
        var min_row = m
        var min_col = n
        for op in ops {
            min_row = min(min_row, op[0])
            min_col = min(min_col, op[1])
        }
        return min_row * min_col
    }
}

