private class Solution {
    func selfDividingNumbers(_ left: Int, _ right: Int) -> [Int] {
        var result: [Int] = []
        for num in left...right {
            if isSelfDividing(num) {
                result.append(num)
            }
        }
        return result
    }

    func isSelfDividing(_ num: Int) -> Bool {
        var numCopy = num
        while numCopy > 0 {
            let digit = numCopy % 10
            if digit == 0 || num % digit != 0 {
                return false
            }
            numCopy /= 10
        }
        return true
    }
}

