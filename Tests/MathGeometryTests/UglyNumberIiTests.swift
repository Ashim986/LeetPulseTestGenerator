private class Solution {
    func nthUglyNumber(_ n: Int) -> Int {
        var count = 0
        var num = 1

        while true {
            if isUgly(num) {
                count += 1
                if count == n { return num }
            }
            num += 1
        }
    }

    func isUgly(_ num: Int) -> Bool {
        var n = num

        n = divideOut(n, 2)
        n = divideOut(n, 3)
        n = divideOut(n, 5)

        return n == 1
    }

    func divideOut(_ num: Int, _ divisor: Int) -> Int {
        var n = num

        while n % divisor == 0 {
            n /= divisor
        }

        return n
    }
}

