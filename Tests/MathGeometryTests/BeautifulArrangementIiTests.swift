private class Solution {
    func constructArray(n: Int) -> [Int] {
        var res: [Int] = Array(1...n)
        var start = 1
        var end = n - 1
        while start <= end {
            res.insert(res.remove(at: end), at: start + 1)
            start += 2
            end -= 2
        }
        return res
    }
}

