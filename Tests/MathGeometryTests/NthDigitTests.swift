private class Solution {
    func findNthDigit(_ n: Int) -> Int {
        var n = n
        var length = 1
        var count = 9
        var start = 1
        while n > length * count {
            n -= length * count
            length += 1
            count *= 10
            start *= 10
        }
        start += (n - 1) / length
        let str = String(start)
        let index = str.index(str.startIndex, offsetBy: (n - 1) % length)
        return Int(String(str[index])) ?? 0
    }
}

