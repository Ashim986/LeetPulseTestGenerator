private class Solution {
    func shuffle(_ nums: [Int]) -> [Int] {
        var numsCopy = nums
        for i in stride(from: numsCopy.count - 1, to: 0, by: -1) {
            let j = Int.random(in: 0...i)
            numsCopy.swapAt(i, j)
        }
        return numsCopy
    }
}

