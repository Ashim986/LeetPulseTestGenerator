enum LC_longest_palindrome {
    private class Solution {
        func longestPalindrome(_ s: String) -> Int {
            let chars = Array(s)
            var count: [Character: Int] = [:]
            for char in chars {
                count[char, default: 0] += 1
            }
            var length = 0
            var odd = false
            for (_, value) in count {
                if value % 2 == 0 {
                    length += value
                }
                else {
                    length += value - 1
                    odd = true
                }
            }
            if odd {
                return length + 1
            }
            else {
                return length
            }
        }
    }

