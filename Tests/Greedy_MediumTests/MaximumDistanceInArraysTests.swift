private class Solution {
    func maxDistToClosest(seats: [Int]) -> Int {
        var maxDist = 0
        var last1 = -1
        for i in 0..<seats.count {
            if seats[i] == 1 {
                if last1 == -1 {
                    maxDist = max(maxDist, i)
                } else {
                    maxDist = max(maxDist, (i - last1) / 2)
                }
                last1 = i
            }
        }
        maxDist = max(maxDist, seats.count - 1 - last1)
        return maxDist
    }
}

