private class Solution {
    func canReorderDoubled(_ arr: [Int]) -> Bool {
        var count: [Int: Int] = [:]

        for num in arr {
            count[num, default: 0] += 1
        }

        let sortedKeys = Array(count.keys).sorted { abs($0) < abs($1) }

        for key in sortedKeys {
            if count[key] == 0 {
                continue
            }

            let double = key * 2
            if count[double] == nil || count[key]! > (count[double] ?? 0) {
                return false
            }

            count[double] = (count[double] ?? 0) - count[key]!
        }

        return true
    }
}

