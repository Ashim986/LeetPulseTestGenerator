private class Solution {
    func canCompleteCircuit(_ gas: [Int], _ cost: [Int]) -> Int {
        let n = gas.count

        for start in 0..<n {
            var tank = 0
            var success = true

            for i in 0..<n {
                let station = (start + i) % n
                tank += gas[station] - cost[station]
                if tank < 0 {
                    success = false
                    break
                }
            }

            if success { return start }
        }

        return -1
    }
}

