private class Solution {
    func strWithout3a3b(_ A: Int, _ B: Int) -> String {
        var res = String()
        var a = A
        var b = B
        while a > 0 || b > 0 {
            if res.count >= 2, res.last == res[res.index(before: res.index(before: res.endIndex))] {
                if res.last == "A" {
                    if b > 0 {
                        res += "B"
                        b -= 1
                    }
                    else {
                        res += "A"
                        a -= 1
                    }
                }
                else {
                    if a > 0 {
                        res += "A"
                        a -= 1
                    }
                    else {
                        res += "B"
                        b -= 1
                    }
                }
            }
            else {
                if a >= b {
                    if a > 0 {
                        res += "A"
                        a -= 1
                    }
                }
                else {
                    if b > 0 {
                        res += "B"
                        b -= 1
                    }
                }
            }
        }
        return res
    }
}

