private class Solution {
    func partitionLabels(_ s: String) -> [Int] {
        let chars = Array(s)
        var last: [Character: Int] = [:]
        for (i, c) in chars.enumerated() { last[c] = i }
        var result: [Int] = []
        var start = 0, end = 0
        for (i, c) in chars.enumerated() {
            end = max(end, last[c]!)
            if i == end {
                result.append(end - start + 1)
                start = i + 1
            }
        }
        return result
    }
}

