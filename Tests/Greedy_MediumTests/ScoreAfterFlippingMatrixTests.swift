private class Solution {
    func matrixScore(_ A: [[Int]]) -> Int {
        var A = A
        let rows = A.count
        let cols = A[0].count
        var res = 0
        for i in 0..<rows {
            if A[i][0] == 0 {
                A[i] = A[i].map { 1 - $0 }
            }
        }
        for j in 1..<cols {
            var ones = 0
            for i in 0..<rows {
                if A[i][j] == 1 {
                    ones += 1
                }
            }
            if ones < rows - ones {
                for i in 0..<rows {
                    A[i][j] = 1 - A[i][j]
                }
            }
        }
        for i in 0..<rows {
            for j in 0..<cols {
                res += A[i][j]
            }
        }
        return res
    }
}

