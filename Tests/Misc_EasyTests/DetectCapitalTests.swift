private class Solution {
    func detectCapitalUse(_ word: String) -> Bool {
        return word == word.uppercased() || word == word.lowercased() || (word.prefix(1) == word.prefix(1).uppercased() && word.dropFirst() == word.dropFirst().lowercased())
    }
}

