enum LC_number_of_islands {
    private class Solution {
        func numIslands(_ grid: [[Character]]) -> Int {
            var grid = grid
            let rows = grid.count
            let cols = grid.first?.count ?? 0
            var count = 0

            func dfs(_ r: Int, _ c: Int) {
                if r < 0 || r >= rows || c < 0 || c >= cols { return }
                if grid[r][c] != "1" { return }
                grid[r][c] = "0"
                dfs(r + 1, c)
                dfs(r - 1, c)
                dfs(r, c + 1)
                dfs(r, c - 1)
            }

            for r in 0..<rows {
                for c in 0..<cols {
                    if grid[r][c] == "1" {
                        count += 1
                        dfs(r, c)
                    }
                }
            }
            return count
        }
    }

