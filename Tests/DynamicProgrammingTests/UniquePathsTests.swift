private class Solution {
    func uniquePaths(_ m: Int, _ n: Int) -> Int {
        var dp = [Int](repeating: 1, count: n)

        for _ in 1..<m {
            for j in 1..<n {
                dp[j] += dp[j-1]
            }
        }

        return dp[n-1]
    }
}

