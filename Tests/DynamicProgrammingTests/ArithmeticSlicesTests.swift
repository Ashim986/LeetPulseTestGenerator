private class Solution {
    func numberOfArithmeticSlices(_ nums: [Int]) -> Int {
        var count = 0
        var dp = [Int: Int]()
        for i in 2..<nums.count {
            let diff = nums[i] - nums[i - 1]
            for j in (0...i - 2) {
                let prevDiff = nums[j + 1] - nums[j]
                if prevDiff == diff {
                    if let currCount = dp[j] {
                        count += currCount
                        dp[i] = (dp[i] ?? 0) + currCount
                    }
                }
            }
            dp[i] = (dp[i] ?? 0) + 1
        }
        return count
    }
}

