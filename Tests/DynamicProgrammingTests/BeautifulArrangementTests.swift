private class Solution {
    func countArrangement(_ n: Int) -> Int {
        var count = 0
        var visited = 0
        func dfs(_ index: Int) {
            if index == n + 1 {
                count += 1
                return
            }
            for i in 1...n {
                if (visited & (1 << (i - 1))) == 0 && (index % i == 0 || i % index == 0) {
                    visited |= (1 << (i - 1))
                    dfs(index + 1)
                    visited &= ~(1 << (i - 1))
                }
            }
        }
        dfs(1)
        return count
    }
}

