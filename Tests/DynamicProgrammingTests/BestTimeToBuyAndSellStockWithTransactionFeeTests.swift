private class Solution {
    func maxProfit(_ prices: [Int], _ fee: Int) -> Int {
        if prices.count < 2 {
            return 0
        }
        var buy = -prices[0]
        var sell = 0
        for i in 1..<prices.count {
            let newBuy = Swift.max(buy, sell - prices[i])
            let newSell = Swift.max(sell, buy + prices[i] - fee)
            buy = newBuy
            sell = newSell
        }
        return sell
    }
}

