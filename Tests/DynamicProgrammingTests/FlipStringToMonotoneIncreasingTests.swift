private class Solution {
    func minFlipsMonoIncr(_ s: String) -> Int { 
        var zeros = 0
        var ones = 0
        for char in s { 
            if char == "0" { 
                zeros = min(ones + 1, zeros)
            } else { 
                ones += 1
            }
        }
        return zeros
    }
}

