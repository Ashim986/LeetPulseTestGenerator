private class Solution {
    func maxTurbulenceSize(_ arr: [Int]) -> Int {
        if arr.count == 1 {
            return 1
        }
        var up = 1
        var down = 1
        var res = 1
        for i in 1..<arr.count {
            if arr[i] > arr[i - 1] {
                up = down + 1
                down = 1
            } else if arr[i] < arr[i - 1] {
                down = up + 1
                up = 1
            } else {
                up = 1
                down = 1
            }
            res = max(res, max(up, down))
        }
        return res
    }
}

