private class Solution {
    func lenLongestFibSubseq(_ A: [Int]) -> Int {
        let set = Set(A)
        var maxLength = 0
        for i in 0..<A.count {
            for j in i+1..<A.count {
                var x = A[i], y = A[j]
                var length = 2
                while true {
                    let z = x + y
                    guard set.contains(z) else {
                        break
                    }
                    x = y
                    y = z
                    length += 1
                }
                if length > 2 {
                    maxLength = max(maxLength, length)
                }
            }
        }
        return maxLength
    }
}

