private class Solution {
    func hIndex(_ citations: [Int]) -> Int {
        let n = citations.count
        var low = 0
        var high = n
        while low <= high {
            let mid = low + (high - low) / 2
            if citations.count - mid <= mid && citations[citations.count - mid] >= mid {
                low = mid + 1
            } else {
                high = mid - 1
            }
        }
        return low
    }
}

