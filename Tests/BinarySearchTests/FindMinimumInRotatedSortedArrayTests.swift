private class Solution {
    func findMin(_ nums: [Int]) -> Int {
        var left = 0
        var right = nums.count - 1

        while left < right {
            let mid = left + (right - left) / 2

            if nums[mid] > nums[right] {
                // Minimum is in the right half
                left = mid + 1
            } else {
                // Minimum is in the left half (including mid)
                right = mid
            }
        }

        return nums[left]
    }
}

