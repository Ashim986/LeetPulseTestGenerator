private class Solution {
    func numSubarrayBoundedMax(_ nums: [Int], _ left: Int, _ right: Int) -> Int {
        var res = 0, start = 0, count = 0, last = -1
        for end in 0..<nums.count {
            if nums[end] > right {
                res += count
                start = end + 1
                last = end
                count = 0
            } else if nums[end] >= left {
                if last >= start {
                    count = end - last
                } else {
                    count = end - start + 1
                }
            }
            if last >= start {
                res += count
            }
        }
        return res
    }
}

