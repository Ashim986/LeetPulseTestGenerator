enum LC_next_permutation {
    private class Solution {
        func nextPermutation(_ nums: inout [Int]) {
            let n = nums.count
            var i = n - 2
            while i >= 0 && nums[i] >= nums[i+1] { i -= 1 }
            if i >= 0 {
                var j = n - 1
                while nums[j] <= nums[i] { j -= 1 }
                nums.swapAt(i, j)
            }
            nums[(i+1)...].reverse()
        }
    }

