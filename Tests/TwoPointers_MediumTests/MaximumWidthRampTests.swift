enum LC_maximum_width_ramp {
    private class Solution {
        func maxWidthRamp(_ nums: [Int]) -> Int { 
         var stack: [Int] = [] 
         var maxWidth: Int = 0 
         for i in 0..<nums.count { 
         while !stack.isEmpty && nums[stack.last!] > nums[i] { 
         let index: Int = stack.popLast()! 
         let width: Int = i - index 
         if width > maxWidth { 
         maxWidth = width 
         } 
         } 
         stack.append(i) 
         } 
         return maxWidth 
         }
    }

