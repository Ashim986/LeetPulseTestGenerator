private class Solution {
    func magicalString(n: Int) -> Int {
        var res = [1]
        var p = 0
        var num = 2
        var count = 1
        while res.count < n {
            for _ in 1...res[p] {
                if res.count < n {
                    res.append(num)
                }
                if num == 1 {
                    count += 1
                }
            }
            num = num == 1 ? 2 : 1
            p += 1
        }
        return count
    }
}

