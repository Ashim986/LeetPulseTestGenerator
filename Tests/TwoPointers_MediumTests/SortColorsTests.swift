private class Solution {
    func sortColors(_ nums: inout [Int]) {
        var count = [0, 0, 0]

        for num in nums {
            count[num] += 1
        }

        var index = 0
        for color in 0..<3 {
            for _ in 0..<count[color] {
                nums[index] = color
                index += 1
            }
        }
    }
}

