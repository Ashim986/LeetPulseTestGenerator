private class Solution {
    func bagOfTokensScore(_ tokens: [Int], _ P: Int) -> Int {
        let n = tokens.count
        var maxScore = 0
        var score = 0
        var points = P
        var left = 0
        var right = n - 1
        let tokens = tokens.sorted()
        while left <= right {
            if points >= tokens[left] {
                points -= tokens[left]
                score += 1
                left += 1
                maxScore = max(maxScore, score)
            }
            else if score > 0 {
                points += tokens[right]
                score -= 1
                right -= 1
            }
            else {
                break
            }
        }
        return maxScore
    }
}

