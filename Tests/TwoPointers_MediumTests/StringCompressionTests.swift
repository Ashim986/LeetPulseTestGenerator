enum LC_string_compression {
    private class Solution {
        func compress(_ chars: inout [Character]) -> Int {
            let n = chars.count
            if n == 0 {
                return 0
            }
            var left = 0
            var write = 0
            for (right, char) in chars.enumerated() {
                if right == n - 1 || char != chars[right + 1] {
                    chars[write] = char
                    write += 1
                    if right > left {
                        let count = right - left + 1
                        for c in String(count) {
                            chars[write] = c
                            write += 1
                        }
                    }
                    left = right + 1
                }
            }
            return min(write, n)
        }
    }

