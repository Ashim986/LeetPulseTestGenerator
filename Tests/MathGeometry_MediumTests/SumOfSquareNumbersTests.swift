private class Solution {
    func judgeSquareSum(_ c: Int) -> Bool {
        var set = Set<Int>()
        var i = 0
        while i * i <= c {
            set.insert(i * i)
            i += 1
        }
        for square in set {
            if set.contains(c - square) {
                return true
            }
        }
        return false
    }
}

