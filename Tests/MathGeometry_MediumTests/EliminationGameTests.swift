private class Solution {
    func lastRemaining(n: Int) -> Int {
        var head = 1
        var remaining = n
        var isForward = true
        var step = 1
        while remaining > 1 {
            if isForward || remaining % 2 == 1 {
                head += step
            }
            step *= 2
            remaining /= 2
            isForward.toggle()
        }
        return head
    }
}

