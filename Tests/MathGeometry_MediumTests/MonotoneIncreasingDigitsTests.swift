private class Solution {
    func monotoneIncreasingDigits(_ N: Int) -> Int {
        var digits = [Int]()
        var n = N
        while n > 0 {
            digits.append(n%10)
            n /= 10
        }
        for i in (1..<digits.count).reversed() {
            if digits[i] < digits[i-1] {
                digits[i-1] -= 1
                for j in (i..<digits.count) {
                    digits[j] = 9
                }
            }
        }
        var res = 0
        for digit in digits.reversed() {
            res = res*10 + digit
        }
        return res
    }
}

