private class Solution {
    func numTrees(_ n: Int) -> Int {
        let n = n
        var dp = Array(repeating: 0, count: n + 1)
        dp[0] = 1
        dp[1] = 1

        for i in 2...n {
            for j in 0...i - 1 {
                dp[i] += dp[j] * dp[i - 1 - j]
            }
        }

        return dp[n]
    }
}

