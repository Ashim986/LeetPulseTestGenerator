private class Solution {
    func flipLights(n: Int, presses: Int) -> Int {
        if n == 0 || presses == 0 {
            return 1
        }
        if n == 1 {
            return 2
        }
        if n == 2 {
            return presses == 1 ? 3 : 4
        }
        let masks: [Int] = [0b11111111, 0b10101010, 0b11001100, 0b10010010]
        var result: Set<Int> = []
        for i in 0...15 {
            var cur = (1 << n) - 1
            for _ in 0...presses {
                cur = cur ^ masks[i % 4]
            }
            result.insert(cur)
        }
        return result.count
    }
}

